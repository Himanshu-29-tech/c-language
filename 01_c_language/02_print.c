#include <stdio.h>
int main(){
  printf("Integer: %d\n",10);//Use \n within the string to move to a new line.
  printf("Character: %c\n",'A');
  printf("Floating-point: %f\n",3.14);

  return 0;

//  %d or %i : for integers
// • %c : for characters
// • %f : for decimal numbers
}

